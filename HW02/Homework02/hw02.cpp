#include <iostream>
using namespace std;

int main()
{

	double var1, var2, var3, var4, var5, sum;

	var1 = 28;
	var2 = 32;
	var3 = 37;
	var4 = 24;
	var5 = 33;

	sum = var1 + var2 + var3 + var4 + var5;

	cout << "The sum of the 5 variables is " << sum << endl;
	cout << "The average of the 5 variables is " << sum / 5 << endl;

	return 0;
}



